package com.zizohanto.popularmovies.data.database.review;

import android.arch.persistence.room.Entity;
import android.arch.persistence.room.Ignore;
import android.arch.persistence.room.PrimaryKey;

import com.google.gson.annotations.SerializedName;

@Entity(tableName = "review")
public class Review {

    @PrimaryKey(autoGenerate = true)
    private int reviewRoomId;

    @SerializedName("author")
    private String author;

    @SerializedName("content")
    private String content;

    @SerializedName("id")
    private String id;

    @SerializedName("url")
    private String url;

    @Ignore
    public Review(String author, String content, String id, String url) {
        this.author = author;
        this.content = content;
        this.id = id;
        this.url = url;
    }

    public Review(int reviewRoomId, String author, String content, String id, String url) {
        this.reviewRoomId = reviewRoomId;
        this.author = author;
        this.content = content;
        this.id = id;
        this.url = url;
    }

    public int getReviewRoomId() {
        return reviewRoomId;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}
